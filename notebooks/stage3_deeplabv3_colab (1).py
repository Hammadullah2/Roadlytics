"""## Cell 13 — Vectorise Predicted Mask → Shapefile
Converts `segmentation_mask_predicted.tif` to `predicted_roads.shp` — the polyline Shapefile consumed by Stage 4.

Attributes produced: `road_id` (int), `length_m` (float). Stage 4 will add `condition` and `confidence`. Stage 5 will add `connected_component_id`.
"""

import rasterio
import rasterio.features
import geopandas as gpd
from shapely.geometry import shape, MultiPolygon
from shapely.ops import unary_union
import warnings
warnings.filterwarnings('ignore')

def vectorise_mask():
    print("Reading predicted mask...")
    with rasterio.open(PRED_MASK_PATH) as src:
        binary = src.read(1)           # (H, W) uint8
        transform = src.transform
        crs = src.crs

    print(f"CRS: {crs}  |  Road pixels: {binary.sum():,}")

    # ── Step 1: Polygonize ─────────────────────────────────────────────────
    print("Polygonizing...")
    shapes = list(rasterio.features.shapes(binary, mask=(binary == 1), transform=transform))
    print(f"Raw polygons: {len(shapes)}")

    polygons = [shape(geom) for geom, val in shapes if val == 1]

    # ── Step 2: Filter tiny noise polygons (< 200 m² = ~2 pixels at 10m) ──
    min_area = 200  # square metres
    polygons = [p for p in polygons if p.area >= min_area]
    print(f"After noise filter (≥{min_area} m²): {len(polygons)} polygons")

    if not polygons:
        print("WARNING: No polygons remain after filtering. Check threshold and prediction quality.")
        return None

    # ── Step 3: Extract boundaries (polygon → linestring) ──────────────────
    lines = []
    for poly in polygons:
        boundary = poly.boundary
        # Simplify to remove pixel staircase artefacts (10m tolerance = 1 pixel)
        simplified = boundary.simplify(tolerance=10.0, preserve_topology=True)
        if simplified.is_empty:
            continue
        # Handle MultiLineString boundaries
        if simplified.geom_type == 'MultiLineString':
            lines.extend(list(simplified.geoms))
        else:
            lines.append(simplified)

    # ── Step 4: Filter very short segments (< 20m) ────────────────────────
    min_length = 20.0  # metres
    lines = [l for l in lines if l.length >= min_length]
    print(f"Linestrings after length filter (≥{min_length}m): {len(lines)}")

    # ── Step 5: Build GeoDataFrame and save ───────────────────────────────
    gdf = gpd.GeoDataFrame({
        'road_id':  list(range(len(lines))),
        'length_m': [round(l.length, 2) for l in lines],
        'geometry': lines
    }, crs=crs)   # EPSG:32642 — required for Stage 4 spatial joins

    gdf.to_file(PRED_SHP_PATH)
    print(f"\nShapefile saved: {PRED_SHP_PATH}")
    print(f"Total road segments: {len(gdf)}")
    print(f"Total road length: {gdf['length_m'].sum()/1000:.2f} km")
    return gdf

