"""## Cell 9 — Loss Function & IoU Metric"""

import torch
import torch.nn.functional as F
import segmentation_models_pytorch as smp

# ── Loss Functions ──────────────────────────────────────────────────────────
# Focal Loss: down-weights easy background pixels, forces focus on hard road pixels
# Dice Loss:  directly optimises pixel overlap — ideal for class-imbalanced masks
# Combined:   60% Dice + 40% Focal gives strong road detection signal

focal_loss_fn = smp.losses.FocalLoss(mode='binary', alpha=0.25, gamma=2.0)
dice_loss_fn  = smp.losses.DiceLoss(mode='binary')

def combined_loss(logits, targets):
    """
    logits:  raw model output  (B, 1, H, W)
    targets: ground truth mask (B, 1, H, W)  values {0.0, 1.0}
    Returns: scalar loss
    """
    focal = focal_loss_fn(logits, targets)
    dice  = dice_loss_fn(logits, targets)
    return 0.4 * focal + 0.6 * dice


def iou_score(logits, targets, threshold=THRESHOLD):
    """
    Returns scalar IoU for a batch.
    Target metric: 65–75% on held-out test set (NFR-11).
    """
    pred   = (torch.sigmoid(logits) > threshold).float()
    smooth = 1e-6
    intersection = (pred * targets).sum()
    union        = pred.sum() + targets.sum() - intersection
    return ((intersection + smooth) / (union + smooth)).item()


print("Loss function and IoU metric defined.")

