"""## Cell 7 — Dataset & DataLoaders"""

import torch
from torch.utils.data import Dataset, DataLoader
import albumentations as A
from sklearn.model_selection import train_test_split
import numpy as np

# ── Augmentation pipeline (training only) ──────────────────────────────────
train_transform = A.Compose([
    # We stripped out GaussNoise and ShiftScaleRotate so they stop destroying your rare roads
    A.HorizontalFlip(p=0.5),
    A.VerticalFlip(p=0.5),
    A.RandomRotate90(p=0.5),
    A.RandomBrightnessContrast(brightness_limit=0.15, contrast_limit=0.15, p=0.4),
])

class RoadTileDataset(Dataset):
    def __init__(self, image_paths, mask_paths, augment=False):
        assert len(image_paths) == len(mask_paths)
        self.image_paths = image_paths
        self.mask_paths  = mask_paths
        self.augment     = augment

    def __len__(self):
        return len(self.image_paths)

    def __getitem__(self, idx):
        image = np.load(self.image_paths[idx])   # (4, TILE_SIZE, TILE_SIZE) float32
        mask  = np.load(self.mask_paths[idx])    # (1, TILE_SIZE, TILE_SIZE) float32

        # ── Topology Trick: Label Dilation ──
        # Ground truth roads are often just 1 pixel wide. This makes the model 'hesitate'
        # and create dotted lines. Dilating to 3px wide forces it to learn a continuous band.
        import cv2
        mask_hw = mask[0]
        mask_hw = cv2.dilate(mask_hw, np.ones((3, 3), np.uint8), iterations=1)

        if self.augment:
            # Albumentations expects HWC format
            image_hwc = image.transpose(1, 2, 0)
            aug = train_transform(image=image_hwc, mask=mask_hw)
            image = aug['image'].transpose(2, 0, 1).astype(np.float32)
            mask_hw  = aug['mask']

        mask = mask_hw[np.newaxis].astype(np.float32)
        return torch.from_numpy(image), torch.from_numpy(mask)


def get_dataloaders():
    img_paths = list((TILES_DIR / 'images').glob('*.npy'))
    mask_paths = list((TILES_DIR / 'masks').glob('*.npy'))

    img_names = set(p.name for p in img_paths)
    mask_names = set(p.name for p in mask_paths)
    common = sorted(list(img_names & mask_names))

    assert len(common) > 0, f"No paired tiles found in {TILES_DIR}. Run Cell 6 first."
    if len(img_paths) != len(mask_paths):
        print(f"Warning: Found {len(img_paths)} images and {len(mask_paths)} masks. Using {len(common)} intersecting.")

    all_img_paths  = [str(TILES_DIR / 'images' / n) for n in common]
    all_mask_paths = [str(TILES_DIR / 'masks' / n) for n in common]

    img_tv, img_test, mask_tv, mask_test = train_test_split(
        all_img_paths, all_mask_paths,
        test_size=TEST_SPLIT, random_state=RANDOM_SEED
    )
    img_train, img_val, mask_train, mask_val = train_test_split(
        img_tv, mask_tv,
        test_size=VAL_SPLIT / (1 - TEST_SPLIT), random_state=RANDOM_SEED
    )

    print(f"Train: {len(img_train)} | Val: {len(img_val)} | Test: {len(img_test)}")

    np.save(OUTPUT_DIR / 'test_img_paths.npy',  np.array(img_test))
    np.save(OUTPUT_DIR / 'test_mask_paths.npy', np.array(mask_test))

    train_ds = RoadTileDataset(img_train, mask_train, augment=True)
    val_ds   = RoadTileDataset(img_val,   mask_val,   augment=False)
    test_ds  = RoadTileDataset(img_test,  mask_test,  augment=False)

    train_loader = DataLoader(train_ds, batch_size=BATCH_SIZE, shuffle=True,
                              num_workers=NUM_WORKERS, pin_memory=True)
    val_loader   = DataLoader(val_ds,   batch_size=BATCH_SIZE, shuffle=False,
                              num_workers=NUM_WORKERS, pin_memory=True)
    test_loader  = DataLoader(test_ds,  batch_size=BATCH_SIZE, shuffle=False,
                              num_workers=NUM_WORKERS, pin_memory=True)

    return train_loader, val_loader, test_loader

train_loader, val_loader, test_loader = get_dataloaders()
