"""## Cell 6 — Tile Generator
Slices `sindh_stack.tif` and `road_mask.tif` into 256×256 `.npy` patches saved to Google Drive.

**Runtime:** ~10–20 minutes for a 10,000×10,000 image with stride=128.
**Skip this cell** if you already ran it and the tiles folder is populated.
"""

import rasterio
from tqdm.notebook import tqdm

def generate_tiles():
    # Check if tiles already exist
    existing = list((TILES_DIR / 'images').glob('*.npy'))
    if existing:
        print(f"Found {len(existing)} existing tiles. Delete {TILES_DIR} to regenerate.")
        return len(existing)

    print("Opening GeoTIFFs...")
    with rasterio.open(STACK_PATH) as src:
        image = np.clip(src.read().astype(np.float32), 0, 10000) / REFLECTANCE_SCALE # (4, H, W)
        H, W = src.height, src.width

    with rasterio.open(MASK_PATH) as src:
        mask = src.read(1).astype(np.float32)   # (H, W)

    # Load cloud mask if it exists
    cloud = None
    if CLOUD_MASK_PATH.exists():
        with rasterio.open(CLOUD_MASK_PATH) as src:
            cloud = src.read(1).astype(np.uint8)  # (H, W), 1 = cloudy
        print(f"Cloud mask loaded. Cloudy pixels: {cloud.sum():,}")

    print(f"Image shape: {image.shape}  |  Mask shape: {mask.shape}")

    rows = range(0, H - TILE_SIZE + 1, STRIDE)
    cols = range(0, W - TILE_SIZE + 1, STRIDE)
    total_positions = len(rows) * len(cols)

    saved = 0
    skipped_cloud = 0
    skipped_empty = 0

    with tqdm(total=total_positions, desc='Generating tiles') as pbar:
        for r in rows:
            for c in cols:
                img_patch  = image[:, r:r+TILE_SIZE, c:c+TILE_SIZE]   # (4, 256, 256)
                mask_patch = mask[r:r+TILE_SIZE, c:c+TILE_SIZE]        # (256, 256)

                # Skip cloudy patches
                if cloud is not None:
                    cloud_patch = cloud[r:r+TILE_SIZE, c:c+TILE_SIZE]
                    if cloud_patch.any():
                        skipped_cloud += 1
                        pbar.update(1)
                        continue

                # Skip patches with almost no road
                road_ratio = mask_patch.sum() / (TILE_SIZE * TILE_SIZE)
                if road_ratio < MIN_ROAD_RATIO:
                    skipped_empty += 1
                    pbar.update(1)
                    continue

                fname = f'patch_{r:05d}_{c:05d}.npy'
                np.save(TILES_DIR / 'images' / fname, img_patch)
                np.save(TILES_DIR / 'masks'  / fname, mask_patch[np.newaxis])  # (1,256,256)
                saved += 1
                pbar.update(1)

    print(f"\nDone. Saved: {saved} | Skipped (cloud): {skipped_cloud} | Skipped (empty): {skipped_empty}")
    return saved

n_tiles = generate_tiles()
