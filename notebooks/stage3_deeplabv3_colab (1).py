def run_custom_inference(input_path, output_path):
    if not input_path.exists():
        print(f"Error: Could not find {input_path}. Did you upload it to the 'raw' folder?")
        return

    print(f"Loading model weights for {input_path.name}...")
    model.load_state_dict(torch.load(CKPT_DIR / 'best_model.pth', map_location=device))
    model.eval()

    with rasterio.open(input_path) as src:
        profile = src.profile.copy()
        H, W = src.height, src.width
        print(f"Input raster: {H} × {W} pixels")

        pred_sum = np.zeros((H, W), dtype=np.float32)
        pred_count = np.zeros((H, W), dtype=np.float32)

        # Process the image in overlapping patches
        total_tiles = len(range(0, H, STRIDE)) * len(range(0, W, STRIDE))
        with tqdm(total=total_tiles, desc='Running custom inference') as pbar:
            for r in range(0, H, STRIDE):
                for c in range(0, W, STRIDE):
                    r_end = min(r + TILE_SIZE, H)
                    c_end = min(c + TILE_SIZE, W)
                    window = rasterio.windows.Window(c, r, c_end - c, r_end - r)
                    patch = src.read(window=window).astype(np.float32) / REFLECTANCE_SCALE

                    # Pad edges if necessary
                    ph, pw = patch.shape[1], patch.shape[2]
                    if ph < TILE_SIZE or pw < TILE_SIZE:
                        padded = np.zeros((IN_CHANNELS, TILE_SIZE, TILE_SIZE), dtype=np.float32)
                        padded[:, :ph, :pw] = patch
                        patch = padded

                    # Inference
                    with torch.no_grad():
                        tensor = torch.from_numpy(patch).unsqueeze(0).to(device)
                        logit = model(tensor)
                        prob = torch.sigmoid(logit).squeeze().cpu().numpy()

                    # Accumulate predictions
                    pred_sum[r:r_end, c:c_end] += prob[:r_end-r, :c_end-c]
                    pred_count[r:r_end, c:c_end] += 1.0
                    pbar.update(1)

        # Average overlapping tiles
        pred_avg = pred_sum / np.maximum(pred_count, 1e-6)
        binary_mask = (pred_avg > THRESHOLD).astype(np.uint8)

    # Save output
    profile.update({'dtype': 'uint8', 'count': 1, 'nodata': 255, 'compress': 'lzw'})
    with rasterio.open(output_path, 'w', **profile) as dst:
        dst.write(binary_mask, 1)

    print(f"\nSuccess! Predicted mask saved to: {output_path}")

# Run the function
