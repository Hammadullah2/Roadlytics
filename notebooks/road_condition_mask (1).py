image_padded = np.pad(image, ((0,0),(pad,pad),(pad,pad)), mode='reflect')

# Find all pixels where the road exists
road_pixels = np.argwhere(road_mask == 1)
print(f"Found {len(road_pixels):,} road pixels to predict. This may take a few minutes...")

# ==========================================
# 3. INFERENCE DATASET & DATALOADER
# ==========================================
class InferenceDataset(Dataset):
    def __init__(self, padded_image, pixels, patch_size):
        self.image = padded_image
        self.pixels = pixels
        self.patch_size = patch_size

    def __len__(self):
        return len(self.pixels)

    def __getitem__(self, idx):
        r, c = self.pixels[idx]
        # Extract patch (r and c are automatically offset correctly in the padded image)
        patch = self.image[:, r:r+self.patch_size, c:c+self.patch_size]
        return torch.from_numpy(patch.astype(np.float32)), r, c

infer_ds = InferenceDataset(image_padded, road_pixels, PATCH_SIZE)
infer_loader = DataLoader(infer_ds, batch_size=BATCH_SIZE, num_workers=NUM_WORKERS, pin_memory=True)

# ==========================================
# 4. RUN PREDICTIONS
# ==========================================
print("\nRunning Deep Learning Predictions...")
# Create a blank array to hold our predictions
prediction_map = np.zeros_like(road_mask, dtype=np.uint8)

with torch.no_grad():
    for patches, rows, cols in tqdm(infer_loader, desc='Predicting'):
        patches = patches.to(device)
        # Get predictions
        logits = model(patches)
        preds = logits.argmax(1).cpu().numpy()

        # We add 1 because our K-Means classes were saved as 1, 2, 3 (not 0, 1, 2)
        preds_for_map = preds + 1

        # Place predictions back into their exact map coordinates
        prediction_map[rows.numpy(), cols.numpy()] = preds_for_map

# ==========================================
# 5. SAVE PREDICTION TIFF
# ==========================================
print(f"\nSaving prediction map to: {OUTPUT_PRED_TIF.name}")
meta.update(count=1, dtype='uint8', nodata=0)
OUTPUT_PRED_TIF.parent.mkdir(parents=True, exist_ok=True)
with rasterio.open(OUTPUT_PRED_TIF, 'w', **meta) as dst:
    dst.write(prediction_map, 1)

