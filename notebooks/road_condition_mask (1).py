from google.colab import drive
drive.mount('/content/drive')
import os
import random
import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
from pathlib import Path

# Machine Learning & Metrics
from sklearn.model_selection import train_test_split
from sklearn.metrics import classification_report, confusion_matrix

# Geospatial
import rasterio

# Deep Learning
import torch
import torch.nn as nn
from torch.utils.data import Dataset, DataLoader, WeightedRandomSampler
from torchvision import models
import albumentations as A

# Utilities (auto-detects Jupyter/Colab environment for nice progress bars)
from tqdm.auto import tqdm


# ==========================================
# CONFIGURATION
# ==========================================
DRIVE_ROOT     = Path('/content/drive/MyDrive/FYP2')
STACK_PATH     = DRIVE_ROOT / 'data' / 'raw' / 'sindh_stack.tif'
COND_MASK_PATH = DRIVE_ROOT / 'data' / 'raw' / 'road_condition_mask.tif'
# Use Colab's local storage for tiny files to speed up I/O significantly
PATCHES_DIR    = Path('/content/condition_patches')

NUM_CLASSES    = 3
PATCH_SIZE     = 32
STRIDE         = 16
REFLECTANCE_SCALE = 10000.0

# ==========================================
# PHASE 2: EXTRACT TRAINING PATCHES
# ==========================================
print("\n" + "="*50)
print("PHASE 2: EXTRACT TRAINING PATCHES")
print("="*50)

for cls in range(NUM_CLASSES):
    (PATCHES_DIR / f'class_{cls}' / 'images').mkdir(parents=True, exist_ok=True)

print("Loading Sentinel-2 stack...")
with rasterio.open(STACK_PATH) as src:
    image = np.clip(src.read().astype(np.float32), 0, 10000) / REFLECTANCE_SCALE

print("Loading K-Means condition mask...")
with rasterio.open(COND_MASK_PATH) as src:
    cond_mask = src.read(1).astype(np.int32)

pad = PATCH_SIZE // 2
image_padded = np.pad(image, ((0,0),(pad,pad),(pad,pad)), mode='reflect')

counts = {cls: 0 for cls in range(NUM_CLASSES)}

# K-Means clusters are stored as 1, 2, 3. Filter and apply stride.
road_pixels = np.argwhere((cond_mask >= 1) & (cond_mask <= NUM_CLASSES))[::STRIDE]

print("Extracting patches...")
for r, c in tqdm(road_pixels, desc='Extracting patches'):
    label = int(cond_mask[r, c])
    if label < 1 or label > NUM_CLASSES:
        continue

    cls_idx = label - 1   # convert 1-indexed K-Means label to 0-indexed class folder

    # Extract patch (shifted by pad since image was padded)
    patch = image_padded[:, r:r+PATCH_SIZE, c:c+PATCH_SIZE]
    if patch.shape != (4, PATCH_SIZE, PATCH_SIZE):
        continue

    fname = f'r{r:05d}_c{c:05d}.npy'
    np.save(PATCHES_DIR / f'class_{cls_idx}' / 'images' / fname, patch)
    counts[cls_idx] += 1

print("Extraction complete!")
for cls, cnt in counts.items():
    print(f"  Class {cls}: {cnt:,} patches")

# ==========================================
# PHASE 3: DATASET & DATALOADERS
# ==========================================
print("\n" + "="*50)
print("PHASE 3: DATASET & DATALOADERS")
print("="*50)

BATCH_SIZE = 256
NUM_WORKERS = 2 # Reduced slightly for Colab stability
RANDOM_SEED = 42

cond_transform = A.Compose([
    A.HorizontalFlip(p=0.5),
    A.VerticalFlip(p=0.5),
    A.RandomRotate90(p=0.5),
    A.RandomBrightnessContrast(brightness_limit=0.1, contrast_limit=0.1, p=0.3),
])

class ConditionPatchDataset(Dataset):
    def __init__(self, paths_and_labels, augment=False):
        self.items = paths_and_labels
        self.augment = augment

    def __len__(self):
        return len(self.items)

    def __getitem__(self, idx):
        path, label = self.items[idx]
        patch = np.load(path)
        if self.augment:
            patch_hwc = patch.transpose(1, 2, 0)
            aug = cond_transform(image=patch_hwc)
            patch = aug['image'].transpose(2, 0, 1).astype(np.float32)
        return torch.from_numpy(patch.astype(np.float32)), torch.tensor(label, dtype=torch.long)

# Collect all paths and labels
all_items = []
for cls in range(NUM_CLASSES):
    cls_dir = PATCHES_DIR / f'class_{cls}' / 'images'
    for p in cls_dir.glob('*.npy'):
        all_items.append((str(p), cls))

random.Random(RANDOM_SEED).shuffle(all_items)
print(f"Total patches: {len(all_items)}")

# Split Dataset
train_items, test_items = train_test_split(all_items, test_size=0.2, random_state=RANDOM_SEED, stratify=[x[1] for x in all_items])
train_items, val_items = train_test_split(train_items, test_size=0.15, random_state=RANDOM_SEED, stratify=[x[1] for x in train_items])
print(f"Train: {len(train_items)} | Val: {len(val_items)} | Test: {len(test_items)}")

# Weighted sampler for class imbalance
class_counts = [sum(1 for _, lbl in train_items if lbl == c) for c in range(NUM_CLASSES)]
class_weights = [1.0 / max(cnt, 1) for cnt in class_counts]
sample_weights = [class_weights[lbl] for _, lbl in train_items]
sampler = WeightedRandomSampler(sample_weights, num_samples=len(sample_weights), replacement=True)

train_ds = ConditionPatchDataset(train_items, augment=True)
val_ds   = ConditionPatchDataset(val_items,   augment=False)
test_ds  = ConditionPatchDataset(test_items,  augment=False)

train_loader = DataLoader(train_ds, batch_size=BATCH_SIZE, sampler=sampler, num_workers=NUM_WORKERS, pin_memory=True)
val_loader   = DataLoader(val_ds,   batch_size=BATCH_SIZE, shuffle=False,   num_workers=NUM_WORKERS, pin_memory=True)
test_loader  = DataLoader(test_ds,  batch_size=BATCH_SIZE, shuffle=False,   num_workers=NUM_WORKERS, pin_memory=True)

# ==========================================
# PHASE 4: EFFICIENTNET-B2 TRAINING
# ==========================================
print("\n" + "="*50)
print("PHASE 4: EFFICIENTNET-B2 TRAINING")
print("="*50)

COND_LR = 1e-3
COND_EPOCHS = 50
COND_PATIENCE = 10
COND_CKPT_PATH = DRIVE_ROOT / 'outputs' / 'road_condition_model.pth'
COND_CKPT_PATH.parent.mkdir(parents=True, exist_ok=True)

device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')

# Build EfficientNet-B2
backbone = models.efficientnet_b2(weights='IMAGENET1K_V1')

# Patch stem conv to 4 channels
old_conv = backbone.features[0][0]
new_conv = nn.Conv2d(4, old_conv.out_channels, kernel_size=old_conv.kernel_size,
                     stride=old_conv.stride, padding=old_conv.padding, bias=False)
with torch.no_grad():
    new_conv.weight[:, :3] = old_conv.weight
    new_conv.weight[:, 3]  = old_conv.weight.mean(dim=1)
backbone.features[0][0] = new_conv

# Replace head
in_features = backbone.classifier[1].in_features
backbone.classifier[1] = nn.Linear(in_features, NUM_CLASSES)

model_cond = backbone.to(device)
print(f"EfficientNet-B2 built. Parameters: {sum(p.numel() for p in model_cond.parameters()):,}")

criterion = nn.CrossEntropyLoss()
optimizer = torch.optim.AdamW(model_cond.parameters(), lr=COND_LR, weight_decay=1e-4)
scheduler = torch.optim.lr_scheduler.CosineAnnealingLR(optimizer, T_max=COND_EPOCHS)

best_val_acc = 0.0
no_improve = 0

print(f"\nTraining on {device} for up to {COND_EPOCHS} epochs...")

for epoch in range(1, COND_EPOCHS + 1):
    # --- TRAIN ---
    model_cond.train()
    total_loss, correct, total = 0.0, 0, 0
    for patches, labels in tqdm(train_loader, desc=f'Epoch {epoch:02d} train', leave=False):
        patches, labels = patches.to(device), labels.to(device)
        optimizer.zero_grad()
        logits = model_cond(patches)
        loss = criterion(logits, labels)
        loss.backward()
        optimizer.step()

        total_loss += loss.item()
        correct += (logits.argmax(1) == labels).sum().item()
        total += labels.size(0)

    train_loss = total_loss / len(train_loader)
    train_acc = correct / total

    # --- VALIDATE ---
    model_cond.eval()
    v_loss, v_correct, v_total = 0.0, 0, 0
    with torch.no_grad():
        for patches, labels in val_loader:
            patches, labels = patches.to(device), labels.to(device)
            logits = model_cond(patches)
            v_loss += criterion(logits, labels).item()
            v_correct += (logits.argmax(1) == labels).sum().item()
            v_total += labels.size(0)

    val_loss = v_loss / len(val_loader)
    val_acc = v_correct / v_total

    scheduler.step()

    print(f"Epoch {epoch:02d}/{COND_EPOCHS} | Train Loss: {train_loss:.4f} Acc: {train_acc:.4f} | Val Loss: {val_loss:.4f} Acc: {val_acc:.4f}")

    if val_acc > best_val_acc:
        best_val_acc = val_acc
        torch.save(model_cond.state_dict(), COND_CKPT_PATH)
        print(f"  ✓ New best val acc: {best_val_acc:.4f} — saved!")
        no_improve = 0
    else:
        no_improve += 1
        if no_improve >= COND_PATIENCE:
            print(f"\nEarly stopping at epoch {epoch}.")
            break

# ==========================================
# PHASE 5: TEST EVALUATION
# ==========================================
print("\n" + "="*50)
print("PHASE 5: TEST SET EVALUATION")
print("="*50)

model_cond.load_state_dict(torch.load(COND_CKPT_PATH, map_location=device))
model_cond.eval()

all_preds, all_labels = [], []
with torch.no_grad():
    for patches, labels in tqdm(test_loader, desc='Test eval'):
        patches = patches.to(device)
        preds = model_cond(patches).argmax(1).cpu().numpy()
        all_preds.extend(preds)
        all_labels.extend(labels.numpy())

label_names = ['Good (Cluster 1)', 'Unpaved (Cluster 2)', 'Damaged (Cluster 3)']
print("\nClassification Report:")
print(classification_report(all_labels, all_preds, target_names=label_names))

print("Generating Confusion Matrix...")
cm = confusion_matrix(all_labels, all_preds)
plt.figure(figsize=(7, 5))
sns.heatmap(cm, annot=True, fmt='d', cmap='Blues', xticklabels=label_names, yticklabels=label_names)
plt.title('Road Condition Classifier — Confusion Matrix')
plt.xlabel('Predicted Label')
plt.ylabel('True Label')
plt.tight_layout()
plt.show()

print("\nPipeline Complete! 🎉")

